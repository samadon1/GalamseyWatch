"""v2 eyeball eval, compare v2 fine-tuned vs v1 fine-tuned vs base.

Three-way comparison to see whether the enriched descriptions + multi-task
training actually fix v1's failure modes:
  - v1 false-negatives on single-pit scenes
  - v1 pit-count under-estimation on multi-pit scenes
  - v1 memorized template variety (2 unique strings → now 1,676 for v2)

Checkpoint paths are hardcoded because leap-finetune uses the same base
naming pattern for both v1 and v2 (differentiated only by timestamp).

Usage:
    uv run modal run scripts/eyeball_eval_v2_modal.py

Expected cost: ~$0.30 (loads 3 models sequentially, ~5-8 min H100).
"""

from __future__ import annotations

import modal

MODAL_VOLUME_NAME = "galamsey"
MODAL_MOUNT_POINT = "/galamsey"

# v1 checkpoint (fully qualified, known from the 2026-04-15 v1 training run)
V1_CHECKPOINT = (
    "/galamsey/lfm2.5-VL-450M-vlm_sft-galamsey_v-all-lr2em05-w0p0-no_lora-20260415_213456"
    "/lfm2.5-VL-450M-vlm_sft-galamsey_v-all-lr2em05-w0p0-no_lora-e3s1110-20260415_213456"
)

# v2 checkpoint (from the 2026-04-16 v2 training run, epoch 3 step 2217)
V2_CHECKPOINT = (
    "/galamsey/lfm2.5-VL-450M-vlm_sft-galamsey_v-all-lr2em05-w0p0-no_lora-20260415_231743"
    "/lfm2.5-VL-450M-vlm_sft-galamsey_v-all-lr2em05-w0p0-no_lora-e3s2217-20260415_231743"
)

# Base model from HuggingFace
BASE_MODEL = "LiquidAI/LFM2.5-VL-450M"

# v2 eval data (generated by prepare_v2_dataset_modal.py)
DESC_EVAL = "/galamsey/data/v2/galamsey_v2_description_eval.jsonl"
GROUNDING_EVAL = "/galamsey/data/v2/galamsey_v2_grounding_eval.jsonl"
IMAGE_ROOT = "/galamsey/data/v2/images"

N_SAMPLES = 10

app = modal.App("galamsey-eyeball-eval-v2")
volume = modal.Volume.from_name(MODAL_VOLUME_NAME, create_if_missing=True)

image = (
    modal.Image.debian_slim(python_version="3.12")
    .pip_install(
        "torch>=2.5",
        "torchvision",
        "transformers>=4.51",
        "pillow",
        "huggingface_hub",
        "accelerate",
    )
)


@app.function(
    image=image,
    volumes={MODAL_MOUNT_POINT: volume},
    gpu="H100",
    timeout=2400,
    secrets=[modal.Secret.from_name("huggingface-secret")],
)
def run_eyeball() -> dict:
    import copy
    import json
    import os
    from pathlib import Path

    import torch
    from PIL import Image
    from transformers import AutoModelForImageTextToText, AutoProcessor

    print("=" * 72)
    print("GalamseyWatch, Eyeball eval v2: v2 vs v1 vs base")
    print("=" * 72)

    v2_checkpoint = V2_CHECKPOINT
    print(f"\nUsing v2 checkpoint: {v2_checkpoint}")
    print(f"Using v1 checkpoint: {V1_CHECKPOINT}")
    print(f"Using base model:    {BASE_MODEL}")
    if not Path(v2_checkpoint).exists():
        raise RuntimeError(
            f"V2 checkpoint not found at {v2_checkpoint}. "
            f"Check the path in the V2_CHECKPOINT constant."
        )

    # Load eval samples, load BOTH description and grounding variants
    def load_samples(jsonl_path: str, n: int) -> list[dict]:
        samples = []
        with open(jsonl_path) as f:
            for i, line in enumerate(f):
                if i >= n:
                    break
                samples.append(json.loads(line))
        return samples

    desc_samples = load_samples(DESC_EVAL, N_SAMPLES)
    grd_samples = load_samples(GROUNDING_EVAL, N_SAMPLES)
    print(f"\nLoaded {len(desc_samples)} description + {len(grd_samples)} grounding samples")

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Device: {device}")

    def prepare_inputs(messages: list[dict], processor) -> tuple[dict, str]:
        messages = copy.deepcopy(messages)
        ground_truth = messages[-1]["content"][0]["text"]
        prompt_messages = messages[:-1]
        for msg in prompt_messages:
            for item in msg.get("content", []):
                if isinstance(item, dict) and item.get("type") == "image":
                    img_ref = item["image"]
                    if not img_ref.startswith("/"):
                        img_ref = f"{IMAGE_ROOT}/{img_ref}"
                    item["image"] = Image.open(img_ref).convert("RGB")
        inputs = processor.apply_chat_template(
            [prompt_messages],
            tokenize=True,
            return_dict=True,
            return_tensors="pt",
            add_generation_prompt=True,
        )
        inputs = {k: v.to(device) for k, v in inputs.items() if v is not None}
        return inputs, ground_truth

    def run_model(model_name_or_path: str, label: str) -> dict[str, list[str]]:
        print(f"\n{'=' * 72}")
        print(f"Loading {label}: {model_name_or_path}")
        print("=" * 72)

        processor = AutoProcessor.from_pretrained(model_name_or_path, trust_remote_code=True)
        model = AutoModelForImageTextToText.from_pretrained(
            model_name_or_path,
            torch_dtype=torch.bfloat16,
            trust_remote_code=True,
        ).to(device)
        model.eval()

        desc_preds: list[str] = []
        grd_preds: list[str] = []

        with torch.no_grad():
            print(f"\n--- [{label}] DESCRIPTION TASK ---")
            for i, sample in enumerate(desc_samples):
                inputs, ground_truth = prepare_inputs(sample["messages"], processor)
                prompt_len = inputs["input_ids"].shape[1]
                output_ids = model.generate(
                    **inputs,
                    max_new_tokens=256,
                    do_sample=False,
                )
                prediction = processor.tokenizer.decode(
                    output_ids[0, prompt_len:], skip_special_tokens=True
                ).strip()
                desc_preds.append(prediction)
                print(f"\n  [desc {i}]")
                print(f"    GT  : {ground_truth}")
                print(f"    PRED: {prediction}")

            print(f"\n--- [{label}] GROUNDING TASK ---")
            for i, sample in enumerate(grd_samples):
                inputs, ground_truth = prepare_inputs(sample["messages"], processor)
                prompt_len = inputs["input_ids"].shape[1]
                output_ids = model.generate(
                    **inputs,
                    max_new_tokens=256,
                    do_sample=False,
                )
                prediction = processor.tokenizer.decode(
                    output_ids[0, prompt_len:], skip_special_tokens=True
                ).strip()
                grd_preds.append(prediction)
                print(f"\n  [grd {i}]")
                print(f"    GT  : {ground_truth[:120]}{'...' if len(ground_truth) > 120 else ''}")
                print(f"    PRED: {prediction[:120]}{'...' if len(prediction) > 120 else ''}")

        del model
        torch.cuda.empty_cache()
        return {"description": desc_preds, "grounding": grd_preds}

    v2_results = run_model(v2_checkpoint, "v2 fine-tuned")
    v1_results = run_model(V1_CHECKPOINT, "v1 fine-tuned")
    base_results = run_model(BASE_MODEL, "base LFM2.5-VL-450M")

    # Three-way summary for description task
    print("\n" + "=" * 72)
    print("DESCRIPTION TASK, three-way summary")
    print("=" * 72)
    for i, sample in enumerate(desc_samples):
        gt = sample["messages"][-1]["content"][0]["text"]
        print(f"\n[desc {i}]")
        print(f"  GT  : {gt}")
        print(f"  v2  : {v2_results['description'][i]}")
        print(f"  v1  : {v1_results['description'][i]}")
        print(f"  base: {base_results['description'][i][:140]}{'...' if len(base_results['description'][i]) > 140 else ''}")

    # Three-way summary for grounding task
    print("\n" + "=" * 72)
    print("GROUNDING TASK, three-way summary")
    print("=" * 72)
    for i, sample in enumerate(grd_samples):
        gt = sample["messages"][-1]["content"][0]["text"]
        print(f"\n[grd {i}]")
        print(f"  GT  : {gt[:200]}{'...' if len(gt) > 200 else ''}")
        print(f"  v2  : {v2_results['grounding'][i][:200]}{'...' if len(v2_results['grounding'][i]) > 200 else ''}")
        print(f"  v1  : {v1_results['grounding'][i][:200]}{'...' if len(v1_results['grounding'][i]) > 200 else ''}")
        print(f"  base: {base_results['grounding'][i][:200]}{'...' if len(base_results['grounding'][i]) > 200 else ''}")

    # Variety summary
    v2_desc_unique = len(set(v2_results["description"]))
    v1_desc_unique = len(set(v1_results["description"]))
    base_desc_unique = len(set(base_results["description"]))
    print(f"\nDescription unique predictions across {N_SAMPLES} samples:")
    print(f"  v2:   {v2_desc_unique}")
    print(f"  v1:   {v1_desc_unique}")
    print(f"  base: {base_desc_unique}")

    return {
        "v2_checkpoint": v2_checkpoint,
        "n_samples": N_SAMPLES,
        "description": {
            "v2_unique": v2_desc_unique,
            "v1_unique": v1_desc_unique,
            "base_unique": base_desc_unique,
        },
    }


@app.local_entrypoint()
def main() -> None:
    print("Submitting v2 eyeball eval to Modal...")
    result = run_eyeball.remote()
    print(f"\nDone. Summary: {result}")
